package com.excilys.cdb.binding.api;

public class CompanyREST {
	private long id;
	private String name;

	public CompanyREST() { }

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
